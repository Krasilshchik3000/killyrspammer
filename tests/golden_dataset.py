# Статистика
_counts = {}
for _, label, _, _ in GOLDEN_DATASET:
    _counts[label] = _counts.get(label, 0) + 1

DATASET_STATS = {
    "total": len(GOLDEN_DATASET),
    "counts": _counts,
    "ratios": {k: f"{v/len(GOLDEN_DATASET)*100:.0f}%" for k, v in _counts.items()},
}


def get_dataset_by_category(category: str) -> list:
    """Получить примеры по категории."""
    return [(t, l, c, d) for t, l, c, d in GOLDEN_DATASET if c == category]


def get_spam_examples() -> list:
    """Все спам-примеры."""
    return [(t, l, c, d) for t, l, c, d in GOLDEN_DATASET if l == "СПАМ"]


def get_not_spam_examples() -> list:
    """Все НЕ_СПАМ примеры."""
    return [(t, l, c, d) for t, l, c, d in GOLDEN_DATASET if l == "НЕ_СПАМ"]


if __name__ == "__main__":
    print(f"Golden Dataset: {DATASET_STATS['total']} примеров")
    for label, count in DATASET_STATS['counts'].items():
        print(f"  {label}: {count} ({DATASET_STATS['ratios'][label]})")
